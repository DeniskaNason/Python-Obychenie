a = int(input('Введите количество секунд: '))
if a > 0 and a < 60:
    print(a, "секунд")
elif a >= 60 and a < 3600:
    b = a // 60
    c = a % 60
    print(b, "минут и", c, "секунд")
elif a >= 3600 and a < 86400:
    b = a // 3600
    c = a // 60 % 60
    d = a // 3600 % 60
    print(b, "Часов", c, "минут", d, "секунд")
elif a >= 86400:
    b = a // 86400
    c = a // 3600 % 24
    d = a // 60 % 60
    e = a % 60
    print(b, "дней", c, "часов", d, "минут", e, "секунд")