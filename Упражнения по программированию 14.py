a = int(input("Введите массу: "))
b = int(input("Укажите ваш рост: "))
c = a / (b**2) * 10000
if c >= 18.5 and c <= 25:
    print("Ваш ИМТ =", f'{c:.2f}', "Это оптимальная масса")
elif c < 18.5 and c > 0:
    print("Ваш ИМТ =", f'{c:.2f}', "Это ниже нормы")
elif c > 25:
    print("Ваш ИМТ =", f'{c:.2f}', "Это избыточная масса")
else:
    print('Хан лох')